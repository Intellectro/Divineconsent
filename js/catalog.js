buttons.forEach(button => {
    button.addEventListener('click', (e)=>{
        const checkID = e.currentTarget.dataset.id;
        const productItems = products.map(product=>{
            if (product.btn_name === checkID){
                return `
                    <div class="cata-data">
                        <img src="${product.image}" alt="#">
                        <div class="cata-product-name">${product.product_name}</div>
                    </div>
                `;
            }
        }).join("")
        document.querySelector('.cata-grid-container').innerHTML = productItems;
    })
})

